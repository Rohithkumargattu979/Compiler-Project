int calcNo(string x){
    int ans=0;
    for(int i=1;i<x.size();i++){
        ans*=10;
        ans+=(x[i]-'0');
    }
    return ans;
}
bool checkifSlash(string x){
  for(int i=0;i<x.size();i++){
        if(x[i]=='/')return false;
    }
    return true;
}
void splitSpaces(string s,vector<string>& arr){
    string curr="";
    for(int i=0;i<s.size();i++){
        if(s[i]==' '){
            arr.push_back(curr);
            curr="";
        }else{
            curr+=s[i];
        }
    }
    if(curr!=""){
        arr.push_back(curr);
    }
}
