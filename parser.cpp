Node parse(){
    //pair<TK_MAIN,0> 0 IS THE STATE
   
   stack<pair<string,int>> st;
   stack<Node> node_st;
   stack<Attr> attr_st;
   Attr a;
   a.value = "";
   a.type = "";
   attr_st.push(a);
   st.push({"",0});
   Node n;
   n.str="";
   node_st.push(n);
   string tk;
   int curr=0;
   Node ans;
   while(curr<inp_tokens.size()){
       tk=inp_tokens[curr];
       if(tk=="@")return ans;
   if(!checkifSlash(mp[{st.top().second,tk}])){
       //handle error
        cout<<"Warning! Please check the grammar if it has R-R or S-R conflicts"<<endl;

   }
   if(mp.find({st.top().second,tk})==mp.end()){
       //report error
        cout<<"Error!: Invalid token, Please check the Syntax"<<endl;
   }else if(mp[{st.top().second,tk}][0]=='s'){
        int n_state=calcNo(mp[{st.top().second,tk}]);//
        cout<< tk<<" " <<n_state<<endl;
      st.push({tk,n_state});
      Node nd;
      nd.str=tk;
      node_st.push(nd);
      curr++;

   }else if(mp[{st.top().second,tk}][0]=='r'){
       int rule_no=calcNo(mp[{st.top().second,tk}]);
       //perform reduce
       vector<string> arr;
       splitSpaces(hm[rule_no],arr);
       
       string left=arr[0];
       Node nd;
       nd.str=left;
        vector<Node> v;
       for(int i=arr.size()-1;i>=1;i--){
            // cout<<arr[i]<<endl;
            if(st.top().first==arr[i]){
                st.pop();
                v.push_back(node_st.top());
                node_st.pop();
            }else{
                //error condition
                 cout<<"Warning!: No match found - improper reduction"<<endl;
            }
       }
       reverse(v.begin() , v.end());
       for(int i=0 ; i<v.size() ; ++i){
           nd.children.push_back(v[i]);
       }
       node_st.push(nd);
       if(nd.str == "PROGRAM"){
           ans = nd;
        }
       cout<<"reducing using"<<" "<<hm[rule_no]<<endl;
       if(mp.find({st.top().second,left})==mp.end()){
           //error
           cout<<st.top().second<<" "<<tk<<" "<<"boot"<<endl;
           cout<<"Warning!: Invalid GO-TO state or no GO-TO available."<<endl;
       }
       if(mp[{st.top().second,left}][0]=='s'||mp[{st.top().second,left}][0]=='r'){
           //error 
           cout<<"Warning!: Improper GO-TO, contains reduction, please check syntax"<<endl;
       }
       
       int goto_state=stoi(mp[{st.top().second,left}]);
       cout<<"going to state "<<goto_state<<endl;
       st.push({left,goto_state});

   }else if(mp[{st.top().second,tk}]=="ACCEPT"){
       cout<<"ACCEPTED "<<endl;

       return ans;
   }
   else{
      //error
      //cout<<"Warning!: No match found"<<endl;
      curr++;
   }
   
   }
  
   return ans;

}
